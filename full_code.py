def hamming_distance(str1, str2):
    """
    Calculates the Hamming distance between two strings.
    If strings are of different lengths, the extra characters count as differences.

    Parameters:
        str1 (str): First string
        str2 (str): Second string

    Returns:
        int: Hamming distance (number of differing characters)
    """

    # Convert both strings to lowercase
    str1 = str1.lower()
    str2 = str2.lower()

    # Find the minimum length to compare characters position-wise
    min_len = min(len(str1), len(str2))

    # Count differences character by character
    distance = sum(1 for i in range(min_len) if str1[i] != str2[i])

    # Add the difference in length (remaining characters are all mismatches)
    distance += abs(len(str1) - len(str2))

    return distance


